/**
 * Calculates the Great Circle distance between two coordinates in km
 * @param {number[]} coord1 [lat, lon]
 * @param {number[]} coord2 [lat, lon]
 * @returns {number} Distance in km
 */
export const calculateHaversineDistance = (coord1, coord2) => {
  const toRad = (x) => (x * Math.PI) / 180;

  const lat1 = coord1[0];
  const lon1 = coord1[1];
  const lat2 = coord2[0];
  const lon2 = coord2[1];

  const R = 6371; // Earth radius in km

  const dLat = toRad(lat2 - lat1);
  const dLon = toRad(lon2 - lon1);

  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) *
    Math.sin(dLon / 2) * Math.sin(dLon / 2);

  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

  return R * c;
};

export const findLocationCoordinates = (query) => {
  if (!query) return null;
  const normalized = query.toLowerCase();
  
  // Exact match or partial match keys
  const key = Object.keys(LOCATIONS).find(k => normalized.includes(k));
  return key ? LOCATIONS[key] : null;
};
